function getPetModels(petNames: Array<string>): Array<PetModel> {
  const result = new Array<PetModel>();
  let id = 0;
  petNames.forEach((petName) => {
    id = id + 1;
    result.unshift(
      new PetModel({
        id: id,
        name: petName,
      })
    );
  });
  return result;
}
